#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    char frase[50] = "AMO FAZER EXERCICIO NO URI";

    printf("<%s>\n", frase);
    printf("<%30s>\n", frase);
    printf("<%.20s>\n", frase);
    printf("<%-20s>\n", frase);
    printf("<%-30s>\n", frase);
    printf("<%.30s>\n", frase);
    printf("<%30.20s>\n", frase);
    printf("<%-30.20s>\n", frase);

    return 0;
}